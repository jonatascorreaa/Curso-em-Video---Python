#Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre sua media
print('Esse é o programa que lê duas notas e calcula sua media')
n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
print(f'Suas notas foram {n1} e {n2}. \nSua media é {(n1+n2)/2:.1f}')
